// There are three keywords to decalre variables

//Var
var varVariables = "This is var variable";
console.log(varVariables);

//Let
let letVariables = "This is let variable";
console.log(letVariables);

letVariables = "This is a reassigned value of let variable";
console.log(letVariables);

//Const

const constVariables = "This is const variable. It cannot be reassigned";
console.log(constVariables);
